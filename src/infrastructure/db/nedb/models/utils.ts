import NeDB from "nedb-promises";

import config from "../../../config";

export function createCollection(name: string) {
    return NeDB.create({
        inMemoryOnly: config.inMemoryDatabase,
        filename: `${config.nedbPath}-${name}`,
        //autoload: true
    })
}